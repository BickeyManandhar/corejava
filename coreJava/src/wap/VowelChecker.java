package wap;

public class VowelChecker {

	public static void main(String[] args) {
		char c='a';
		if(c=='a' || c=='e' || c=='i' || c=='o' ||c=='u') {
			System.out.println("The character '"+c+"' is a vowel.");
		}
		else {
			System.out.println("The character '"+c+"' is not a vowel.");
		}

	}

}
